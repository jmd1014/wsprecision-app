-- 배경:
--   생산 준비 → 발주 자동 제안 흐름에서 어느 수주 때문에 발주가
--   생성됐는지 추적할 컬럼 필요.
--
-- 변경:
--   purchase_orders.remark TEXT — "출처 수주: SO-xxx, SO-yyy" 형식 기록
--   (sales_order FK 가 아닌 텍스트인 이유: 다수 수주가 하나의 발주로
--    묶이는 N:1 구조 + 수기 발주는 출처 없음)
-- ════════════════════════════════════════════════════════════

ALTER TABLE purchase_orders
  ADD COLUMN IF NOT EXISTS remark TEXT;

COMMENT ON COLUMN purchase_orders.remark IS
  '비고. 생산 준비 → 발주 자동 제안 흐름에서는 "출처 수주: SO-..." 기록.';
